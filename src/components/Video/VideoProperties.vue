<template>
  <div>
    <h3>Video properties</h3>
    <b-form>
      <b-form-group
        label="Processing:"
        horizontal>
        <b-input
          :value="processingState"
          disabled />
      </b-form-group>

      <b-form-group
        label="Comment"
        horizontal>
        <b-form-row class="m-0">
          <b-form-textarea
            :value="value"
            class="col"
            @input="$emit('input', $event)" />
          <div class="col-md-1" />
          <b-button
            class="col-md-3"
            @click="updateComment">Save Comment</b-button>
        </b-form-row>
      </b-form-group>
      <b-alert
        :show="showAlert"
        variant="success"
        dismissible
        @dismissed="showAlert=false">Comment saved.</b-alert>

      <b-form-group>
        <b-button
          :block="true"
          variant="danger">Delete Video</b-button>
      </b-form-group>

    </b-form>

    <h3>Download Files</h3>
    <b-button
      :href="downloadRawUrl"
      target="_blank">Download Raw</b-button>
    <b-button
      :href="downloadFileUrl"
      target="_blank">Download File</b-button>

  </div>
</template>

<script>

import api from '../../api/index';
import { Config } from '../../../app.config';

export default {
  // https://vuejs.org/v2/style-guide/#Multi-word-component-names-essential
  name: 'VideoProperties',
  // https://vuejs.org/v2/style-guide/#Prop-definitions-essential
  props: {
    processingState: {
      type: String,
      default: "incomplete"
    },
    value: {
      type: String,
      default: ""
    },
    downloadRaw: {
      type: String,
      default: ""
    },
    downloadFile: {
      type: String,
      default: ""
    }
  },
  // https://vuejs.org/v2/style-guide/#Component-data-essential
  data () {
    return {
      showAlert: false
    };
  },
  // https://vuejs.org/v2/style-guide/#Simple-computed-properties-strongly-recommended
  computed: {
    downloadRawUrl: function () {
      return `${Config.api}/api/v1/signedUrl?jwt=${this.downloadRaw}`;
    },
    downloadFileUrl: function () {
      return `${Config.api}/api/v1/signedUrl?jwt=${this.downloadFile}`;
    }
  },
  methods: {
    updateComment() {
      let token = this.$store.state.User.JWT;
      return new Promise((resolve, reject) => {
        api.recording.comment(this.value, this.$route.params.id, token)
          .then(response => response.json())
          .then((json) => {
            if(!json.success) {
              reject(json);
            } else {
              this.showAlert = true;
              resolve(json);
            }
          });
      });
    }
  }
};
</script>

